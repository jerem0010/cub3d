#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	s;
	int	m;

	i = 0;
	s = 0;
	m = 0;
	while ((str[i] <= 13 && str[i] >= 9) || str[i] == 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		s++;
		if (s >= 2)
			return (0);
		if (str[i] == '-')
			m++;
		i++;
	}
	s = 0;
	while (str[i] >= 48 && str[i] <= 57)
		s = s * 10 + (str[i++] - 48);
	if (m % 2 == 1)
		s = s * -1;
	return (s);
}
